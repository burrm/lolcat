transform.xt.to.individual.format <- function(
  x, 
  row.var="row", 
  col.var = "col", 
  use.weight = F) {
  
  transform.xt.to.independent.format(x= x,
                                     row.var = row.var,
                                     col.var = col.var,
                                     use.weight = use.weight)

}