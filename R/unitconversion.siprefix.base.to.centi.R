#' Unit Conversion - SI Prefixes - Base to Centi-  
#'
#' Centi- hundredths, 1/100, or 10^(-2) 
#' 
#' Performs a conversion from base units to centi-units (ex. grams to centigrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to centi-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.centi <- function(
  x = 1
) {
  x * 100
}

