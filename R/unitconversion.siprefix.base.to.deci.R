#' Unit Conversion - SI Prefixes - Base to Deci-  
#' 
#' Deci- tenths, 1/10, or 10^(-1) 
#'
#' Performs a conversion from base units to deci-units (ex. grams to decigrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to deci-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.deci <- function(
  x = 1
) {
  x * 10
}

