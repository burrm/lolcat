#' Unit Conversion - SI Prefixes - Base to Deka-  
#' 
#' Deka- tens, 10, or 10^1 
#'
#' Performs a conversion from base units to deka-units (ex. grams to dekagrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to deka-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.deka <- function(
  x = 1
) {
  x / 10
}

