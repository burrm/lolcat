#' Unit Conversion - SI Prefixes - Base to Exa-  
#' 
#' Exa- quintillions, 1 000 000 000 000 000 000, or 10^18 
#'
#' Performs a conversion from base units to exa-units (ex. grams to exagrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to exa-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.exa <- function(
  x = 1
) {
  x / (10^18)
}

