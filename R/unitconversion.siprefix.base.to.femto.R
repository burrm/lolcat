#' Unit Conversion - SI Prefixes - Base to Femto-  
#'
#' Femto- quadrillionths, 1/1 000 000 000 000 000, or 10^(-15) 
#' 
#' Performs a conversion from base units to femto-units (ex. grams to femtograms). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to femto-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.femto <- function(
  x = 1
) {
  x * (10^15)
}

