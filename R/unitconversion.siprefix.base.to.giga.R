#' Unit Conversion - SI Prefixes - Base to Giga-  
#' 
#' Giga- billions, 1 000 000 000, or 10^9 
#'
#' Performs a conversion from base units to giga-units (ex. grams to gigagrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to giga-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.giga <- function(
  x = 1
) {
  x / (10^9)
}

