#' Unit Conversion - SI Prefixes - Base to Kilo-  
#' 
#' Kilo- thousands, 1000, or 10^3 
#'
#' Performs a conversion from base units to kilo-units (ex. grams to kilograms). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to kilo-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.kilo <- function(
  x = 1
) {
  x / 1000
}

