#' Unit Conversion - SI Prefixes - Base to Micro-  
#'
#' Micro- millionths, 1/1 000 000, or 10^(-6) 
#' 
#' Performs a conversion from base units to micro-units (ex. grams to micrograms). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to micro-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.micro <- function(
  x = 1
) {
  x * 1000000
}

