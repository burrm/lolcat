#' Unit Conversion - SI Prefixes - Base to Nano-  
#'
#' Nano- billionths, 1/1 000 000 000, or 10^(-9) 
#' 
#' Performs a conversion from base units to nano-units (ex. grams to nanograms). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to nano-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.nano <- function(
  x = 1
) {
  x * (10^9)
}

