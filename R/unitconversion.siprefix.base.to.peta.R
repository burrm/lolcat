#' Unit Conversion - SI Prefixes - Base to Peta-  
#' 
#' Peta- quadrillions, 1 000 000 000 000 000, or 10^15 
#'
#' Performs a conversion from base units to peta-units (ex. grams to petagrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to peta-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.peta <- function(
  x = 1
) {
  x / (10^15)
}

