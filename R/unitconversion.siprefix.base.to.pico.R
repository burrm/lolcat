#' Unit Conversion - SI Prefixes - Base to Pico-  
#'
#' Pico- trillionths, 1/1 000 000 000 000, or 10^(-12) 
#' 
#' Performs a conversion from base units to pico-units (ex. grams to picograms). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to pico-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.pico <- function(
  x = 1
) {
  x * (10^12)
}

