#' Unit Conversion - SI Prefixes - Base to Tera-  
#' 
#' Tera- trillions, 1 000 000 000 000, or 10^12 
#'
#' Performs a conversion from base units to tera-units (ex. grams to teragrams). 
#'
#' @param x Vector - Values in units of base units
#'
#' @return x, but converted to tera-units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.base.to.tera <- function(
  x = 1
) {
  x / (10^12)
}

