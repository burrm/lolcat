#' Unit Conversion - SI Prefixes - Centi- to Base  
#'
#' Centi- hundredths, 1/100, or 10^(-2) 
#' 
#' Performs a conversion from centi-units to base units (ex. centigrams to grams). 
#'
#' @param x Vector - Values in units of centi-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.centi.to.base <- function(
  x = 1
) {
  x / 100
}

