#' Unit Conversion - SI Prefixes - Deci- to Base  
#' 
#' Deci- tenths, 1/10, or 10^(-1) 
#'
#' Performs a conversion from deci-units to base units (ex. decigrams to grams). 
#'
#' @param x Vector - Values in units of deci-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.deci.to.base <- function(
  x = 1
) {
  x / 10
}

