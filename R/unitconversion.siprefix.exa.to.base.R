#' Unit Conversion - SI Prefixes - Exa- to Base  
#' 
#' Exa- quintillions, 1 000 000 000 000 000 000, or 10^18 
#'
#' Performs a conversion from exa-units to base units (ex. exagrams to grams). 
#'
#' @param x Vector - Values in units of exa-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.exa.to.base <- function(
  x = 1
) {
  x * (10^18)
}

