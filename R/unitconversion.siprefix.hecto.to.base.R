#' Unit Conversion - SI Prefixes - Hecto- to Base 
#' 
#' Hecto- hundreds, 100, or 10^2 
#'
#' Performs a conversion from hecto-units to base (ex. hectograms to grams). 
#'
#' @param x Vector - Values in units of hecto-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.hecto.to.base <- function(
  x = 1
) {
  x * 100
}

