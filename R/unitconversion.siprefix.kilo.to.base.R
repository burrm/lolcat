#' Unit Conversion - SI Prefixes - Kilo- to Base  
#' 
#' Kilo- thousands, 1000, or 10^3 
#'
#' Performs a conversion from kilo-units to base units (ex. kilograms to grams). 
#'
#' @param x Vector - Values in units of kilo-units
#'
#' @return x, but converted to base units
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.kilo.to.base <- function(
  x = 1
) {
  x * 1000
}

