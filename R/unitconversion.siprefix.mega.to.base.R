#' Unit Conversion - SI Prefixes - Mega- to Base  
#' 
#' Mega- millions, 1 000 000, or 10^6 
#'
#' Performs a conversion from mega-units to base units (ex. megagrams to grams). 
#'
#' @param x Vector - Values in units of mega-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.mega.to.base <- function(
  x = 1
) {
  x * (10^6)
}

