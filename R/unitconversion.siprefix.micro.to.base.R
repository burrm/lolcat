#' Unit Conversion - SI Prefixes - Micro- to Base  
#'
#' Micro- millionths, 1/1 000 000, or 10^(-6) 
#' 
#' Performs a conversion from micro-units to base units (ex. micrograms to grams). 
#'
#' @param x Vector - Values in units of micro-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.micro.to.base <- function(
  x = 1
) {
  x / 1000000
}

