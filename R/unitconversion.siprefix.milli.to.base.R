#' Unit Conversion - SI Prefixes - Milli- to Base 
#'
#' Milli- thousandths, 1/1000, or 10^(-3) 
#' 
#' Performs a conversion from milli-units to base units (ex. milligrams to grams). 
#'
#' @param x Vector - Values in units of milli-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.milli.to.base <- function(
  x = 1
) {
  x / 1000
}

