#' Unit Conversion - SI Prefixes - Nano- to Base 
#'
#' Nano- billionths, 1/1 000 000 000, or 10^(-9) 
#' 
#' Performs a conversion from nano-units to base units (ex. nanograms to grams). 
#'
#' @param x Vector - Values in units of nano-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.nano.to.base <- function(
  x = 1
) {
  x / (10^9)
}

