#' Unit Conversion - SI Prefixes - Peta- to Base  
#' 
#' Peta- quadrillions, 1 000 000 000 000 000, or 10^15 
#'
#' Performs a conversion from peta-units to base units (ex. petagrams to grams). 
#'
#' @param x Vector - Values in units of peta-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.peta.to.base <- function(
  x = 1
) {
  x * (10^15)
}

