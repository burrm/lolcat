#' Unit Conversion - SI Prefixes - Pico- to Base  
#'
#' Pico- trillionths, 1/1 000 000 000 000, or 10^(-12) 
#' 
#' Performs a conversion from pico-units to base units (ex. picograms to grams). 
#'
#' @param x Vector - Values in units of pico-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.pico.to.base <- function(
  x = 1
) {
  x / (10^12)
}

