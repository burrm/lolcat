#' Unit Conversion - SI Prefixes - Tera- to Base  
#' 
#' Tera- trillions, 1 000 000 000 000, or 10^12 
#'
#' Performs a conversion from tera-units to base units (ex. teragrams to grams). 
#'
#' @param x Vector - Values in units of tera-units
#'
#' @return x, but converted to base units 
#'
#' @references
#' NIST. Metric (SI) Prefixes. 2022. Accessed 4/7/2022. 
#' https://www.nist.gov/pml/weights-and-measures/metric-si-prefixes
unitconversion.siprefix.tera.to.base <- function(
  x = 1
) {
  x * (10^12)
}

